import { Autocomplete, Box, Button, TextField, Typography } from "@mui/material";
import { useTheme } from "@mui/material/styles";

import { TeamCard } from "./TeamCard";
import { Team } from "@services/organization"

interface DeleteChildProps {
  teams: Team[];
  selectedTeam: Team | null;
  onTeamSelect: (team: Team | null) => void;
  onDelete: () => void;
}

export const DeleteChild: React.FC<DeleteChildProps> = ({
  teams,
  selectedTeam,
  onTeamSelect,
  onDelete,
}) => {
  const theme = useTheme();

  return (
    <Box
      sx={{
        display: "flex",
        flexDirection: "column",
        gap: "8px",
        padding: "12px",
      }}
    >
      <Box
        sx={{
          display: "flex",
          flexDirection: "column",
          gap: "2px",
        }}
      >
        <Typography
          variant="body2"
          sx={{
            color: theme.palette.customText.primary.p2.active,
          }}
        >
          Delete teams
        </Typography>

        <Typography
          variant="caption"
          sx={{
            color: theme.palette.customText.primary.p3.active,
            whiteSpace: "pre-wrap",
          }}
        >
          You can delete teams that no longer exist. All employees previously under the team will be
          unassigned from that team.
        </Typography>
      </Box>

      <Box
        sx={{
          display: "flex",
          gap: 2,
          width: "100%",
          justifyContent: "space-between",
        }}
      >

        <Box sx={{
          display: "flex",
          flexDirection: "column",
          flex: .8,
          gap: 1.5
        }}>
          <Autocomplete
            options={teams}
            getOptionLabel={(option) => option.name}
            value={selectedTeam}
            onChange={(_, newValue) => onTeamSelect(newValue)}
            renderInput={(params) => <TextField {...params} placeholder="Select team" />}
            sx={{
              flex: 0.8,
              paddingY: 0,
            }}
          />

          {/* Team Card - Display when team is selected */}
          {selectedTeam && (
            <TeamCard
              teamName={selectedTeam.name}
              headCount={selectedTeam.headCount}
              teamHead={{
                name: "Team Head Name",
                title: "Head of Team",
                avatar: undefined,
              }}
              functionLead={{
                name: "Function Lead",
                title: "Lead Title",
                avatar: undefined,
              }}
            />
          )}
        </Box>

        <Button
          variant="outlined"
          sx={{ height: "fit-content" }}
          color="error"
          onClick={onDelete}
          disabled={!selectedTeam}
        >
          Delete
        </Button>
      </Box>
    </Box>
  );
};
